<h3>Glossary Words:</h3>
<br>
<ul>
<li><b>Hyper Text Markup Language (HTML):</b></li>
<p>Is the standard markup language for documents designed to be displayed in a web browser. It can be assisted by technologies cuh as cascading style sheets (CSS) and scripting languages such as Javascript.</p>
<li><b>Cascading Style Sheet (CSS):</b></li>
<p>Style sheet language used for describing the presentation os a document written in a markup language like HTML. CSS is designed to enable the separation and content, including layout, colors, and fonts. </p>
<li><b>Javascript (JS):</b></li>
<p>Is a programming language that conforms to the ECMA Script specification. Javascript is a high-level, often just-in-time compiled, and multi-paradigm. It has curly-bracket syntax, dynamic typing, prototype-based object-orientation, and first-class functions.</p>
<li><b>Tag and Cotags:</b></li>
<p>A command inserted in a document that specifies how the document, or a portion of the document, should be formatted. Tags are used by all format specifications that store dosuments as text files.</p>
<li><b>Browser:</b></li>
<p>A software program that allows the user to find and read encoded documents in a form suitable for display, especially such a program for use on the world wide web.</p> 
<li><b>Anchor Tag:</b></li>
<p>The HTML code for creating a link to another page or to a particular section within a page. It is also commonly called an "h-ref".</p>
<p>Example Syntax: <a href="http..name..com">Link</a></p>
<li><b>Hypertext Transfer Protocol (HTTP):</b></li>
<p>Is an application protocol for distributed, collaborative, hypermedia information systems.HTTP is the foundation of data communication for the world wide web, where hypertext documents include hyperlinks to other resources that the user can easliy access.</p>




</ul>